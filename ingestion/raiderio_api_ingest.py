# -----------------------------
# PREP OUTPUT FOLDER
# -----------------------------

os.makedirs("data/bronze", exist_ok=True)

timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")

# -----------------------------
# INGEST EACH CHARACTER
# -----------------------------

for character, config in characters.items():

    season = config["season"]
    run_ids = config["run_ids"]

    print(f"\n==== Pulling {character} ({season}) ====")

    runs = []

    for run_id in run_ids:

        print(f"Pulling run {run_id}")

        url = "https://raider.io/api/v1/mythic-plus/run-details"

        params = {
            "access_key": ACCESS_KEY,
            "season": season,
            "id": run_id
        }

        response = requests.get(url, params=params)

        if response.status_code == 200:
            runs.append(response.json())
            print("Success")
        else:
            print(f"Failed -> {response.status_code}")

        time.sleep(0.5)

    filepath = f"data/bronze/{character}_runs_{timestamp}.json"

    with open(filepath, "w") as f:
        json.dump(runs, f, indent=2)

    print(f"Saved {len(runs)} runs to {filepath}")